<p>
	I couldn't just flippantly say a scratchpad of thoughts, a much sought-after extension of my embarrassingly undersized head, unrelated
	figments strewn around, collectibles from the waste basket scraps that should have seen a shredder, or, somewhat more crudely, <b>everything</b> about
	<b>anything</b>. But that's <u>exactly</u> what I always wanted my blog to be. I'm not a prisoner to the usual
	boundaries that limit most people, and I wanted a place where people like me could find a way to
	read my thoughts and not feel alone.
</p>
<p>
	In Latin, an "omnibus" is a volume containing several novels or other items previously published separately. 
This usage likely comes from the idea of bringing together many works "for all" to enjoy. 
"Omnibus" thus implies "for all." <p>"Omnia" means "all things" or "everything."</p> <p>Thus, "Omnibus Omnia" translates to "For Everyone, About
	Anything." And that satisfies the school boy inside me. The phrase expresses the idea of inclusiveness and universal provision. A pompous name for something that could have
	otherwise been ignored as a trifle.
</p>

<p>
	Omnibus Omnia is thus intended as a universal melting pot of information, ideas, and, if I'm
	allowed some more big words, wisdom! In this wide-ranging consortium, the limit is only the ever-expanding
	horizon of my imagination. It encompasses every conceivable topic and field of inquiry as a beacon
	of diverse and inclusive discourse. 
</p>
